package looping;

public class InfiniteLoop {


    public void howToForLoopForever() {
        for (; ; ) {
            //Business logic
        }

    }

    public void howToWhileLoopForever() {

        while (true) {
            //Business logic
        }

    }

    public void howToDoWhileLoopForever() {

        do {
            //Business logic
        } while (true);

    }

}
